def subtracao(num1, num2):
  '''
  subtracao(): é um método que recebe dois valores, subtrai o primeiro do segundo e retorna um resultado... 
  Exemplo: subtracao(5,3) irá retornar 2, pois é o resultado de 5-3
  '''
  res = num1 - num2
  return res


def soma(num1, num2):
  '''
  soma(): é um método que soma dois valores e retorna o resultado... 
  Exemplo: soma(5,3) irá retornar 8, pois é o resultado de 5+3
  '''
  res = num1 + num2
  return res
