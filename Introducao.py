# Curso de Aprendizado de Python - Introdução

# Este script de introdução irá demonstrar alguns conceitos básicos em Python
# e receberá valores de variáveis para interação simples.

# Exemplo 1: Variáveis e Entrada de Dados
dia = input("Digite o dia em que nasceu: ")
mes = input("Digite o mes em que nasceu: ")
ano = input("Digite o ano em que nasceu: ")
print("Nasci em %s/%s de %s" % (dia, mes, ano))

# Exemplo 2: Saída de Dados e Comentários
hora   = int(input("Digite a hora atual: "))
minuto = int(input("Digite o minuto atual: "))
print("Agora sao %i:%i" % (hora, minuto))

pi = 3.14159265358
print("O valor de pi é %.5f" % (pi))

